package com.dbw.sixconnectandroid.utils;

/**
 * Created by DBW on 2016/12/8.
 * 保存棋子位置的工具类
 */
public class ChessLocation {
    public int x;
    public int y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
